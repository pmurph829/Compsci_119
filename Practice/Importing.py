# importing
# 3 ways:
# 1) import math           - Need to use math.sqrt() for every funciton
# 2) from math import sqrt - Can use sqrt() only
# 3) from math import *    - Can use all funcitons without math.---()

# 1 is usually the preferred way, 2 is second choice if you only need one function
# 3 is last choice bc it can cause name conflicts if you have defined functions w same name
